package kr.spring.member.domain;

public class CodeCommand {
	private String code_code;
	private String code_email;
	private String code_status;
	
	public String getCode_status() {
		return code_status;
	}
	public void setCode_status(String code_status) {
		this.code_status = code_status;
	}
	public String getCode_code() {
		return code_code;
	}
	public void setCode_code(String code_code) {
		this.code_code = code_code;
	}
	public String getCode_email() {
		return code_email;
	}
	public void setCode_email(String code_email) {
		this.code_email = code_email;
	}
	
	
}
